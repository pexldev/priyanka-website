export function getGalleryImagesByService(service: string): GalleryImage[] {
  return galleryImages.filter(image => image.service === service);
}

export function getFeaturedGalleryImages(count: number = 6): GalleryImage[] {
  return galleryImages.slice(0, count);
}

export function getAllServices(): string[] {
  const services = new Set(galleryImages.map(image => image.service));
  return Array.from(services);
}
